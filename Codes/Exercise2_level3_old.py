def Conflate(arg1):
    "Conflate each line"
    arg2=len(arg1)
    screen=['']*12
    for cycle2 in range(12):
        for cycle3 in range(arg2):
            exec("screen[cycle2]+="+arg1[cycle3]+'[cycle2]')
    return(screen)

